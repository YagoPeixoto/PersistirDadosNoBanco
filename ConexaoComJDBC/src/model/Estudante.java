package model;

import java.awt.List;

public class Estudante {
private int id;
private String nome;
private List livros;

public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public List getLivros() {
	return livros;
}
public void setLivros(List livros) {
	this.livros = livros;
}


	
}
